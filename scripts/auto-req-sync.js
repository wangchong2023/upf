const fs = require('fs');
const { execSync } = require('child_process');

/**
 * 需求同步脚本 v1.0
 * 职责：解析特性清单并自动生成 SRS 与 RTM 矩阵
 */

// Role Gate Check
try {
    execSync('node scripts/mgr-role-gate.js --action=SRS_GEN', { stdio: 'inherit' });
} catch (e) {
    process.exit(1);
}

const content = fs.readFileSync('docs/spec-feature-list.md', 'utf-8');
const lines = content.split('\n');

let rrList = [];
let irList = [];
let srList = [];
let rtmList = [];

let currentL2 = '';
let currentL3 = '';
let currentRR_ID = '';
let currentIR_ID = '';

let rrCounter = 1;
let irCounter = 1;
let srCounter = 1;

for (let line of lines) {
    line = line.trimEnd();
    if (!line) continue;

    if (line.startsWith('## ')) {
        currentL2 = line.substring(3).trim();
        let rrSeq = rrCounter.toString().padStart(3, '0');
        currentRR_ID = `RR.UPF.${rrSeq}`;
        rrList.push(`| **${currentRR_ID}** | 特性清单 | **${currentL2}** | P1 |`);
        rrCounter++;
        irCounter = 1;
    } else if (line.startsWith('### ')) {
        currentL3 = line.substring(4).trim();
        let rrSeq = (rrCounter - 1).toString().padStart(3, '0');
        let irSeq = irCounter.toString().padStart(2, '0');
        currentIR_ID = `IR.UPF.${rrSeq}.${irSeq}`;
        irList.push(`| **${currentIR_ID}** | ${currentRR_ID} | FUN | **${currentL3}** | - |`);
        irCounter++;
        srCounter = 1;
    } else if (line.match(/^[\s]*-\s+(.*)/)) {
        let match = line.match(/^[\s]*-\s+(.*)/);
        let feature = match[1].replace(/[\*~_]/g, '').trim(); 
        if (feature && currentIR_ID) {
             let rrSeq = (rrCounter - 1).toString().padStart(3, '0');
             let irSeq = (irCounter - 1).toString().padStart(2, '0');
             let srSeq = srCounter.toString().padStart(3, '0');
             
             let currentSR_ID = `SR.UPF.${rrSeq}.${irSeq}.${srSeq}`;
             let currentAR_ID = `AR.UPF.${rrSeq}.${irSeq}.${srSeq}.01`;
             
             let trNode = "TR3";
             let leadDays = 45; // Default lead time from TR1 to release
             if (currentRR_ID === "RR.UPF.003") {
                 trNode = "TR2";
                 leadDays = 30;
             }
             if (currentRR_ID === "RR.UPF.004") {
                 trNode = "TR5";
                 leadDays = 60;
             }
             
             // 模拟根据当前日期和 TR 节点计算“承诺交付日期”
             let baseDate = new Date(); 
             let targetDateObj = new Date(baseDate.getTime() + (leadDays * 24 * 60 * 60 * 1000));
             let targetDateStr = targetDateObj.toISOString().split('T')[0];

             feature = feature.replace(/<[^>]*>?/gm, '');
             srList.push(`| **${currentSR_ID}** | ${currentIR_ID} | FUN | **${feature}** | - |`);
             rtmList.push(`| **${currentRR_ID}** | **${currentIR_ID}** | **${currentSR_ID}** | **${currentAR_ID}** | FUN | **cp-core** | **${trNode}** | ${targetDateStr} | \`TC.${currentSR_ID}\` | 待开发 |`);
             srCounter++;
        }
    }
}

let specContent = `# 5G UPF 系统规格说明书 (SRS - IPD 标准 v0.2)\n\n## 1. RR (Raw Requirement) 原始需求\n| 编号 | 来源 | 描述 | 优先级 |\n| :--- | :--- | :--- | :--- |\n${rrList.join('\n')}\n\n## 2. IR (Initial Requirement) 初始需求\n| 编号 | 关联 RR | 类型 | 特性描述 | 目标 |\n| :--- | :--- | :--- | :--- |\n${irList.join('\n')}\n\n## 3. SR (System Requirement) 系统规格\n| 编号 | 关联 IR | 类型 | 规格详述 | 协议遵循 |\n| :--- | :--- | :--- | :--- | :--- |\n${srList.join('\n')}\n\n---\n*Generated by Gemini CLI script based on Complete Product Feature List.*\n`;

fs.writeFileSync('docs/spec-srs.md', specContent);

let rtmContent = `# 5G UPF 功能拆解 with Commitment (RTM - IPD 标准 v0.3)\n\n## 1. RTM (Requirement Traceability Matrix) 全链路追踪矩阵\n\n| RR ID | IR ID | SR ID | AR ID | 类型 | 分配模块 | TR 节点 | 承诺交付 | 测试用例 | 状态 |\n| :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n${rtmList.join('\n')}\n\n## 2. AR (Allocation Requirement) 分配需求详情\n*(由于需求项达数百条，具体 AR 详情将在后续各子系统的架构设计与微服务拆解阶段进行细化展开)*\n\n---\n*Generated by Gemini CLI script based on Deep Decomposition of Product Feature List.*\n`;

fs.writeFileSync('docs/spec-rtm.md', rtmContent);
console.log(`Generated ${rrList.length} RRs, ${irList.length} IRs, ${srList.length} SRs/ARs with TR Node mappings.`);
