const config = require('../core/mgr-config');
/**
 * @职责: 自动补齐的治理脚本
 * @版本: v1.0
 */

const fs = require('fs');
const path = require('path');

/**
 * 需求调度 Agent (Scheduler Agent) v1.1
 * 职责：根据 RTM 承诺交付日期，自动推导下游子任务计划并维护任务看板
 */

try {
    const RTM_PATH = config.PATHS.RTM;
    const TASK_PLAN_PATH = config.PATHS.TASK_PLAN;

    const args = process.argv.slice(2);
    const targetVersion = args.find(a => a.startsWith('--version='))?.split('=')[1];

    console.log(`🚀 [Scheduler Agent] Deriving task schedules ${targetVersion ? `for version ${targetVersion}` : 'for all versions'}...`);

    if (!fs.existsSync(RTM_PATH)) {
        console.error("❌ RTM not found. Run agent-pm first.");
        process.exit(1);
    }

    const rtmContent = fs.readFileSync(RTM_PATH, 'utf-8');
    const lines = rtmContent.split('\n');

    const tasks = [];

    // 提前量定义 (Lead times in days)
    const LEAD_TIMES = {
        SE: 21,    // 需求分解
        ARCH: 14,  // 架构设计
        DEV: 7,    // 开发实现
        TEST: 2    // 测试用例准备
    };

    lines.forEach(line => {
        if (line.includes('| **SR.UPF.')) {
            const parts = line.split('|').map(p => p.trim());
            if (parts.length < 11) return;

            // 过滤逻辑：如果指定了版本且行中不包含该版本标识，则跳过
            if (targetVersion && !line.includes(`[Target: ${targetVersion}]`)) return;

            const srId = parts[3].replace(/\*\*/g, '');
            const arId = parts[4].replace(/\*\*/g, '');
            const trNode = parts[7];
            const targetDateStr = parts[8];
            const targetDate = new Date(targetDateStr);

            if (isNaN(targetDate.getTime())) return;

            /**
 * @职责: 自动补齐的治理函数
 */
const calculateDeadline = (days) => {
                const d = new Date(targetDate.getTime() - (days * 24 * 60 * 60 * 1000));
                return d.toISOString().split('T')[0];
            };

            tasks.push({
                id: arId,
                target: targetDateStr,
                se: calculateDeadline(LEAD_TIMES.SE),
                arch: calculateDeadline(LEAD_TIMES.ARCH),
                dev: calculateDeadline(LEAD_TIMES.DEV),
                test: calculateDeadline(LEAD_TIMES.TEST),
                status: parts[10]
            });
        }
    });

    let taskMarkdown = `# 5G UPF 下游任务自动化排期表 (v1.0)\n\n`;
    taskMarkdown += `> 此表由 Scheduler Agent 根据 RTM 承诺日期自动生成，作为下游角色领取任务的基准。\n\n`;
    taskMarkdown += `| AR ID | 承诺交付 | SE 截止 (分解) | Arch 截止 (设计) | Dev 截止 (实现) | Test 截止 (用例) | 当前状态 |\n`;
    taskMarkdown += `| :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n`;

    tasks.forEach(t => {
        taskMarkdown += `| ${t.id} | ${t.target} | ${t.se} | ${t.arch} | ${t.dev} | ${t.test} | ${t.status} |\n`;
    });

    taskMarkdown += `\n---\n*Generated by Scheduler Agent based on Demand-Driven Pull Mechanism.*\n`;

    fs.writeFileSync(TASK_PLAN_PATH, taskMarkdown);
    console.log(`✅ [Scheduler Agent] Downstream task plan generated at ${TASK_PLAN_PATH} (${tasks.length} items).`);
    console.log("   💡 IMPACT: Suggest calling activate_skill(name='change-management') to assess the risk of task shifting on downstream deadlines.");
} catch (error) {
    console.error(`❌ [Scheduler Agent] Error: ${error.message}`);
    process.exit(1);
}
