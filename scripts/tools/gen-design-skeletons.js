const fs = require('fs');
const path = require('path');
const config = require('../core/mgr-config');

const PATHS = config.PATHS;

/**
 * 生成 ADR 模板
 */
function generateADR(rrId, rrName) {
    const filename = `ADR-${rrId.replace(/\./g, '-')}.md`;
    const targetPath = path.join(PATHS.ADR_DIR, filename);

    if (fs.existsSync(targetPath)) {
        return; // 已存在则跳过
    }

    const template = `# ADR: ${rrName} 架构设计方案

## 1. 背景 (Context)
对应需求: \`${rrId}\`
业务背景: 此处填写该需求引入的背景。

## 2. 备选方案 (Alternatives)
- **Option A**: 描述方案一。
- **Option B**: 描述方案二。

## 3. 架构决策 (Decision)
**选择 Option X**。

## 4. 理由 (Rationale)
- 性能考量。
- 扩展性考量。

## 5. 状态
- **Status**: [Draft / Proposed / Accepted]
- **Date**: ${new Date().toISOString().split('T')[0]}
- **Expert**: TBD

---
*Generated by gen-design-skeletons.js*
`;

    fs.writeFileSync(targetPath, template);
    console.log(`✨ Generated ADR skeleton: ${targetPath}`);
}

/**
 * 主逻辑
 */
function main() {
    try {
        console.log("🚀 Starting Design Skeleton Generation...");

        if (!fs.existsSync(PATHS.SRS)) {
            console.error("❌ SRS file missing.");
            return;
        }

        const content = fs.readFileSync(PATHS.SRS, 'utf-8');
        const lines = content.split('\n');
        
        lines.forEach(line => {
            const match = line.match(/\| (\*\*RR\.UPF\.\d+\*\*) \| [^|]+ \| ([^|]+) \|/);
            if (match) {
                const rrId = match[1].replace(/\*/g, '');
                const rrName = match[2].trim().replace(/\*\*/g, '');
                generateADR(rrId, rrName);
            }
        });

        console.log("✅ Design skeletons up to date.");
    } catch (e) {
        console.error(`❌ [Design Gen] Fatal Error: ${e.message}`);
        process.exit(1);
    }
}

main();
